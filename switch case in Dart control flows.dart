void main()
{
  // switch cases in Dart
  String operator = '+';
  switch(operator ){
    case '+':
      print(2+4);
      break;
    case '-':
      print(4-2);
      break;
    case '*':
      print(4*4);
      break;
    case '/':
      print(10/2);
      break;
    default:
      print("Invalid Arguments");
      break;
  }
}